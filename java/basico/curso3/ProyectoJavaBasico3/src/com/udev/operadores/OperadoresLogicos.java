package com.udev.operadores;

public class OperadoresLogicos{

	public static void main(String[] args) {
		boolean res1 = 10 > 8;
		System.out.println("10 > 8 = " + res1);

		boolean res2 = 10 > 10;
		System.out.println("10 > 10 = " + res2);

		boolean res3 = 10 < 8;
		System.out.println("10 < 8 = " + res3);

		boolean res4 = 6 < 8;
		System.out.println("6 < 8 = " + res4);

		boolean res5 = 10 >= 10 ;
		System.out.println("10 >= 10 = " + res5);

		boolean res6 = 10 <= 10;
		System.out.println("10 <= 10 = " + res6);

		boolean res7 = 10 != 8;
		System.out.println("10 != 8 = " + res7);

		boolean res8 = 10 == 10;
		System.out.println("10 == 10 = " + res8);

	}

}