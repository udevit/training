package com.udev.operadores;

public class OperadorAsignacion{

	public static void main(String[] args) {
		//declaración de variables
		int edad;

		//asignación de valor
		edad = 10;
		edad = 18;
		edad = 35;

		//declaración y asignación en una sola línea
		int edad2 = 28;

		System.out.println("Edad: " + edad);
		System.out.println("Edad2: " + edad2);
	}

}